int countSubstrings(string s)
{
    unordered_set<string> substrings;
    for (int i = 0; i < s.length(); ++i)
    {
        string sub = "";
        for (int j = i; j < s.length(); ++j)
        {
            sub += s[j];
            substrings.insert(sub);
        }
    }
    return substrings.size();
}

